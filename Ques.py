# حروف الفبا
letters = ['الف', 'ب', 'پ', 'ت', 'ث', 'ج', 'چ', 'ح', 'خ', 'د', 'ذ', 'ر', 'ز', 'ژ', 'س', 'ش', 'ص', 'ض', 'ط', 'ظ', 'ع', 'غ', 'ف', 'ق', 'ک', 'گ', 'ل', 'م', 'ن', 'و', 'ه', 'ی']

# لیست سوالات نهایی
questions = []

# تابع برای تولید سوالات
def generate_questions():
    for category in categories:
        for letter in letters:
            question = f"{category} با حرف {letter}"
            questions.append(question)
            if len(questions) >= 1600:  # پس از رسیدن به 1600 سوال متوقف شود
                return

# تولید سوالات
generate_questions()

# ذخیره سوالات در فایل JSON
data = {
    "questions": questions
}

with open('AMJADIQUE.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=4)

